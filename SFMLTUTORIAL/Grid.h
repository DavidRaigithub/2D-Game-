//Method to draw grid
void drawGrid(int grid[GRID_HEIGHT][GRID_WIDTH], sf::RenderWindow& window, sf::Texture& wallTexture) {
    sf::Sprite wallSprite(wallTexture);
    wallSprite.setScale(
        static_cast<float>(32) / wallTexture.getSize().x,
        static_cast<float>(32) / wallTexture.getSize().y
    );

    // Drawing grid walls
    for (int y = 0; y < GRID_HEIGHT; ++y) {
        for (int x = 0; x < GRID_WIDTH; ++x) {
            if (grid[y][x] == 1) {
                wallSprite.setPosition(x * 32, y * 32);
                window.draw(wallSprite);
            }
        }
    }
}

